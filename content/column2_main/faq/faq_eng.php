<div class="column_2_article">
    <p class='bold'>How does MixCont set optimization criteria?</p>
    <p>MixCont is a self-learning system.  The system identifies mixing process behavior and compares the current batch with all previous batches in terms of distribution, dispersion and viscosity.  The system learns from each batch and achieves better results on every subsequent batch until the point where quality parameters cannot be improved further within the given mixing time limits or the mixing time cannot be reduced to achieve the desired quality parameters.  The process will then be stabilized and any mixing changes will only be due to ongoing processing and raw material variations.</p>
</div>

<div class="column_2_article">
