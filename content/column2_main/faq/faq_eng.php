    <p class='bold'>How does MixCont differ from other mixing control systems?</p>
    <p>The MixCont system is unique due to its ability to analyze a compound’s properties and optimize the mixing process while the compound is being mixed in the mixer.  (No other industrial control system that has this capability.)</p>
