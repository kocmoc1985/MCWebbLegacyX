<div class="column_2_article">
    <p class='bold'>How does MixCont work?</p>
    <p>MixCont uses sensors to receive information from the various components of the mixer.  This provides the MixCont algorithm with inputs such as: torque, power, temperature, ram position and pressure, as well as rotor speed.  Based on the user’s objectives, the algorithm then updates the mixer’s control parameters in real time to obtain the compound’s desired properties for each batch using minimum time and energy.  The Mixcont system allows the user to prioritize objectives (productivity versus quality) for each compound.</p>
</div>

<div class="column_2_article">
    <p class='bold'>How does MixCont estimate the properties of a compound?</p>
    <p>MixCont follows the same principle as an Oscillating Disk Rheometer (ODR).  The system estimates the properties using the mixer itself instead of torque rheometry.  It uses the torque value from the mixer’s rotor in combination with temperature, pressure and other signals to build the internal MixCont quality parameters.  These quality parameters correlate to the level of distribution, dispersion and viscosity and are used for control optimization.  MixCont uses these parameters to achieve user designated uniform batch to batch properties.</p>
</div>

<div class="column_2_article">
    <p class='bold'>Are there mixer compatibility issues with MixCont?</p>
    <p>No.  MixCont works with every age and size mixer from the lab to large production models.  Additionally, the system is compatible with tangential, intermix and variable rotor clearance mixers.  MixCont provides gains for any mixer where it is installed.</p>
</div>

<div class="column_2_article">
    <p class='bold'>Are there any compound limitations with MixCont?</p>
    <p>MixCont will work with any plastic or rubber compound. Additionally, the system has been tested on non-polymer compounds in processes such as baking, concrete mixing and oil refining.  The system is effective in any mixing process of unique ingredients.</p>
</div>

