    <h3>Статистический инструмент для обработки и анализа данных относящихся к производству резиновых смесей (MCStats)</h3>

    <p>Осуществляет сбор, обработку, систематизацию, комплексный анализ и визуализацию различного рода данных в форме таблиц, графиков и выводов, сделанных на основе анализа. Например:</p>

    <ol>
        <li>Полная статистическая информация о сырье, автоматическом взвешивании, смешении в смесителе, на вальцах и экструдере, статусе оборудования и результатах тестирования в лаборатории предоставляется одной программой и может выводиться на экран компьютера в любой комбинации и различных формах.
        <li>Определяется причинно-следственная связь между различными параметрами и событиями, например между выходом вязкости отдельных смесей за допустимые пределы и новой поставкой натурального каучука.
        <li>Автоматическое определение неполадок оборудования, внештатных ситуаций во время смешения и прочих проблем на базе комплексного анализа сигналов с обычного набора датчиков установленных на различном оборудовании. Так инструмент автоматически определяет утечку охлаждающей воды в смеситель или о нарушение изоляции кабелей проложенных между смесителем и ”PLC”.
    </ol>

