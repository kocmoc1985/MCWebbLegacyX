    <h3>Уникальные возможности системы «Mixcont Control»</h3>

    <p>Система <strong>«Mixcont Control»</strong> обладает уникальной способностью использовать смеситель в качестве прибора, автоматически отслеживающего изменение вязкости, однородности и степени дисперсии сыпучих ингредиентов во время смешения каждой порции смеси. Наличие такой информации дает возможность системе выбирать такую скорость вращения роторов, давление верхнего пресса, время смешения и т.д., которые обеспечивают заданный уровень вязкости, однородности и степени дисперсии сыпучих ингредиентов для каждой порции смеси за возможно более короткое время.</p>

    <p>Это дает производителям резиновых смесей:</p>

    <ol>
        <li>Минимум 5% экономии времени и энергии на каждую линию смешения
        <li>Существенное улучшение качества любых типов резиновых смесей
        <li>Большая часть проблем с качеством каждой порции смеси становится видимой как только она выгружаеся из смесителя 
    </ol>

