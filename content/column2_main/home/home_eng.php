    <h3>Process analyzing and statistics tools MCStats</h3>
    <p>Our tools collect, process, systematize and make complex analysis of different kind of data related to the production floor. These tools provide the following:</p>
    <ol>
        <li>Complete statistical information about raw materials, mixing in mixer, mills, extruder & laboratory test results. All the mentioned statistical data is available from one single piece of software.
        <li>Cause and effect relationships between different parameters
        <li>Automatic detection of malfunctions, problems during mixing and other abnormal events
        <li>Automatic evaluation of fill factor, dispersion, distribution, viscosity etc.
    </ol>
    <p>For example, MixCont statistical tools show the user the reason why viscosity of a compound got out of specification, informs that the filling factor chosen for a recipe is too low or there is a leakage of water into the mixer chamber etc.</p>

