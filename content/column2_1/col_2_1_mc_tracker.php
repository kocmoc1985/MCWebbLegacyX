<div class="column_2_article">
    <h3>MCTracker</h3>

    <p>MC Labour and Machinery Tracker (MCTracker) is designed for on/off-line supervision of labour and machinery utilisation at one or several mixing lines of the plant floor. It documents time spent on production, set up, maintenance and downtime of the user-defined reasons.</p>

    <p>This tool is dedicated to manages, machine operators and other personal responsible for manufacturing process.</p>

    <p>Data acquisition modules of MCTracker are directly connected to the mechanisms of mixing lines for collecting, processing and continuous real-time monitoring of all the relevant information related to the production performances.</p>

    <p>One of on-line monitors of MCTracker shows effective manufacturing time, downtimes, reasons for downtimes, summarised time for different specific downtime reasons for a batch, order or shift. If maintenance takes place, its type and duration is also shown in the on-line monitor.</p>

    <p>The user can log all kinds of downtimes and maintenance reasons in hierarchical way on each machine. They can be activated automatically or entered manually via MC Tracker’s operator interface.</p>

    <p>To provide reliability and precision to time related data logged by machine operators, MCTracker synchronises the logged data with the process data.</p>

    <p>MCTracker’s on-line monitor for Production plan displays: </p>
    <ol>
        <li>orders planned for the actual and coming dates for different mixing lines, total number of batches in every order and how many of them are already mixed
        <li>planned, calculated and real time spent to produce every of the displayed orders
    </ol>

    <p>Not mixed and already mixed orders are marked with different colours.</p>

    <p>MCTracker generates diverse statistical reports on customers’ request. The reports can present information numerically, in form of graphs, charts, time breakdown screens etc.</p>

    <p>MCTracker is fully integrated with the other MixCont products: MC control system, MC Browser, MC Lab and MC Early Defect Detector. That makes data related to the process, raw materials, test results, detected problems and malfunctions accessible from MCTracker. By request the user can get various reports that combine different kind of information and define its correlation. It enables to avoid searching through huge amount of data, get clear picture of the entire production, be aware about all the bottlenecks and weak points, understand cause and effect relationships.</p>

    <p>The information presented by MCTracker can be accessible for the machine operators and multiple PC workstations.</p>

