    <h3>MCLab</h3>

    <p>«Mixcont Lab» высокоэффективная система по сбору, обработке и анализу лабораторных данных и интегрирации лабораторных данных с другими данными относящимися к технологическому процессу. Эта система создана на основании пожеланий технологов и менеджмента различных резиновых компаний специально для автоматизации лабораторий относящихся к производству резиновых смесей и готовых изделий из резины.</p>
