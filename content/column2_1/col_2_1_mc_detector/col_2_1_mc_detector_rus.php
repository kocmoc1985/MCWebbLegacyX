    <h3>MCStats</h3>

    <p>«MCStats» собирает, обрабатывает, систематизирует и делает комплексный анализ различного рода данных относящихся к производству резиновых смесей, представляя полную структурированную информацию о всем производственном процессе в виде простых и удобных в использовании таблиц, графиков и сообщений. Так, например, статистическая информация о сырье, автоматическом взвешивании, смешении в смесителе, на вальцах и экструдере, статусе оборудования и результатах тестирования в лаборатории как смесей, так и готовых продуктов предоставляется одной программой и может выводиться на экран компьютера в любой комбинации и различных формах.</p>  

